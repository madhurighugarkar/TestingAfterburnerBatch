package patternProgram;

public class NumberPattern {

	public static void main(String[] args) {
		
		int a, n=5;   
		  
		for(int i=0; i<n; i++)  
		{   
		a=1;   
		  
		for(int j=0; j<=i; j++)  
		{   

		System.out.print(a+ " ");   
		   
		a++;   
		}   
		  
		System.out.println();   
		}   
		

	}

}
