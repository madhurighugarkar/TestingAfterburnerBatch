package operatorsProgram;

public class ArithmaticOperator {

	public static void main(String[] args) {
		
		 int a=570;
		 int b=373;
		 
		 //addition operator
		 System.out.println("a+b = " +(a+b));
		
		 //subtraction operator
		 System.out.println("a-b = " +(a-b));
		 
		//multipication operator
		 System.out.println("a*b = " +(a*b));
		 
		//division operator
		 System.out.println("a/b = " +(a/b));
		 
		//modul operator
		 System.out.println("a%b = " +(a%b));

	}

	}


