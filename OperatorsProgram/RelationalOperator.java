package operatorsProgram;

public class RelationalOperator {

	public static void main(String[] args) {
		
		int a=789;
		int b=37;
		
		//greater than
		System.out.println(a>b);
		
		//less than
		System.out.println(a<b);
		
		//equal to equal to
		System.out.println(a==b);
		
		//greater than equal to
		System.out.println(a>=b);
		
		//less than equal to
		System.out.println(a<=b);
		
		//not equal to
		System.out.println(a!=b);

	}

}
