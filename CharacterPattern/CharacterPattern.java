package patternProgram;

public class CharacterPattern {

	public static void main(String[] args) {
		
		char ch='a', n=4;   
		  
		for(int i=0; i<n; i++)  
		{   
		ch='a';   
		  
		for(int j=0; j<=i; j++)  
		{   

		System.out.print(ch+"");   
		   
		ch++;   
		}   
		  
		System.out.println();   
		}   
	
	}

	}


